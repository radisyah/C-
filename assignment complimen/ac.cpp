#include <iostream>

using namespace std;

int main()
{

	//Belajar Operator Assignment

	int a = 10;

	cout << "Nilai Awal a = " << a << endl;
	a += 3;
	cout << "Ditambah 3 = " << a << endl;
	a -= 3;
	cout << "Dikurangi 3 = " << a << endl;
	a *= 3;
	cout << "Dikali 3 = " << a << endl;
	a /= 3;
	cout << "Dibagi 3 = " << a << endl;




	system("Pause");
}
